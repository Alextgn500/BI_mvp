"""
Утилиты для формирования векторов признаков.

Здесь находятся простые преобразования "сырого" ввода в формат, пригодный для модели.
"""

from collections.abc import Iterable


def tofeaturevector(raw: Iterable[float]) -> list[float]:
    """
    Преобразует "сырой" итерируемый ввод в список числовых признаков.

    Args:
        raw (Iterablefloat): последовательность чисел (список/кортеж/генератор).

    Returns:
        Listfloat: список признаков в виде float.

    Notes:
        - Функция не выполняет валидацию типов элементов; предполагается, что элементы
          приводимы к float извне или до вызова.
        - В простом демо её задача — явное преобразование входа в list.
    """
    return list(raw)
